a=int(input("Enter the value 1:"))
b=int(input("Enter the value 2:"))
c=input("Enter the Opr..:")
if c=="+":
    print("Addition is",a+b)
elif c=="-":
    print("Subtraction is",a-b)
elif c=="*":
    print("Multiplication is",a*b)
elif c=="/":
    print("Division is",a/b)
else:
    print("Not Valid")